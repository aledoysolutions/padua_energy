                    <p class="my-3" style="font-size: 20px;">Diesel hybrid is an energy system comprised of diesel generators and batteries. The generator creates energy which goes to usage as well as charging batteries. When the batteries are fully charged the generators stops, and all the energy used comes from the batteries. When the battery nears its minimal or charge level, the generators restart to refill the batteries.</p>
                    <p class="my-3" style="font-size: 20px;">The benefit of such an energy supply is that the generators will be turned off for most of the day and energy production happens under optimal loading of the generators. As a rule of thumb, a generator produces four times more energy per liter of diesel than the generators running at low loads.</p> <br><br>

